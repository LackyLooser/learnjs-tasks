// Оба цикла выведут alert с одинаковыми значениями или нет?

// Постфиксная форма:

// 1

// for (let i = 0; i < 5; i++) alert( i );

// Префиксная форма:

// 2

// for (let i = 0; i < 5; ++i) alert( i );

//----------Ответ-------

// 1 задача выведет 0, 1, 2, 3, 4
// 2 задача выведет 0, 1, 2, 3, 4
